class Track(object):
    def __init__(self, parent, donnees):
        self.IDfamille = donnees[0]
        self.internet_actif = donnees[1]
        self.internet_identifiant = donnees[2]
        self.internet_mdp = donnees[3]

        self.nomTitulaires = parent.dict_titulaires[self.IDfamille]["titulairesSansCivilite"]



class ListView(FastObjectListView):
    def __init__(self, *args, **kwds):
        # Récupération des paramètres perso
        self.selectionID = None
        self.selectionTrack = None
        self.criteres = ""
        self.donnees = []
        self.itemSelected = False
        self.popupIndex = -1
        self.listeFiltres = []
        # Initialisation du listCtrl
        FastObjectListView.__init__(self, *args, **kwds)
        # Binds perso
        self.Bind(wx.EVT_CONTEXT_MENU, self.OnContextMenu)
                        
    def InitModel(self):
        self.donnees = self.GetTracks()

    def GetTracks(self):
        """ Récupération des données """
        DB = GestionDB.DB()
        req = """SELECT IDfamille, internet_actif, internet_identifiant, internet_mdp
        FROM familles
        ;"""
        DB.ExecuterReq(req)
        listeFamilles = DB.ResultatReq()
        DB.Close()

        listeListeView = []
        for item in listeFamilles :
            track = Track(self, item)
            listeListeView.append(track)
            if self.selectionID == item[0] :
                self.selectionTrack = track
        return listeListeView
      
    def InitObjectListView(self):            
        # Couleur en alternance des lignes
        self.oddRowsBackColor = UTILS_Interface.GetValeur("couleur_tres_claire", wx.Colour(240, 251, 237))
        self.evenRowsBackColor = wx.Colour(255, 255, 255)
        self.useExpansionColumn = True

        def FormateActivation(internet_activation):
            if internet_activation == 1 :
                return _(u"Oui")
            else :
                return _(u"Non")

        liste_Colonnes = [
            ColumnDefn(_(u"ID"), "left", 0, "IDfamille", typeDonnee="entier"),
            ColumnDefn(_(u"Famille"), 'left', 280, "nomTitulaires", typeDonnee="texte"),
            ColumnDefn(_(u"Activation"), "left", 100, "internet_actif", typeDonnee="texte", stringConverter=FormateActivation),
            ColumnDefn(_(u"Identifiant"), "left", 100, "internet_identifiant", typeDonnee="texte"),
            ColumnDefn(_(u"Mot de passe"), "left", 100, "internet_mdp", typeDonnee="texte"),
            ]
        
        self.SetColumns(liste_Colonnes)
        self.CreateCheckStateColumn(0)
        self.SetEmptyListMsg(_(u"Aucune famille"))
        self.SetEmptyListMsgFont(wx.FFont(11, wx.DEFAULT, face="Tekton"))
        self.SetSortColumn(self.columns[2])
        self.SetObjects(self.donnees)

    def MAJ(self, ID=None):
        self.dict_titulaires = UTILS_Titulaires.GetTitulaires()
        if ID != None :
            self.selectionID = ID
            self.selectionTrack = None
        else:
            self.selectionID = None
            self.selectionTrack = None
        self.InitModel()
        self.InitObjectListView()
        # Sélection d'un item
        if self.selectionTrack != None :
            self.SelectObject(self.selectionTrack, deselectOthers=True, ensureVisible=True)
        self.selectionID = None
        self.selectionTrack = None

    def Selection(self):
        return self.GetSelectedObjects()

